#include "libft.h"

int		ft_wdmatch(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s2[i])
	{
		if (s2[i] && s1[j] && s2[i] == s1[j])
			j++;
		i++;
	}
	if (!s1[j])
		return (1);
	return (0);
}
